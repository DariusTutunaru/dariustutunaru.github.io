const container = document.getElementById("container");

for (const key in evenimente) {
  if (Object.hasOwnProperty.call(evenimente, key)) {
    const event = evenimente[key];
    
    const card = document.createElement("div");
    
    if (parseInt(key.replace("Eveniment", "")) % 2 === 0) {
      card.classList.add("card-even");

      const cardContent = `
      <div class="right"></div>
        <div class="left">
            <div class="cnt">
                <div class="dl">
                    <h1 class="data">${event.data}</h1>
                    <h1 class="locatie"><i>${event.locatie}</i></h1>
                </div>
                <p class="text">${event.text}</p>
                <img src=${event.img} alt=""></img>
                <figure align="center" class="poza">
                    <img src="${event.fig.img}" alt="">
                    <figcaption>${event.fig.cap}</figcaption>
                </figure> 
                </div>
        </div>
    `;
    card.innerHTML = cardContent;
    } else {
      card.classList.add("card-odd");

      const cardContent = `
      <div class="left">
            <div class="cnt">
                <div class="dl">
                <h1 class="locatie"><i>${event.locatie}</i></h1>
                    <h1 class="data">${event.data}</h1>
                </div>
                <p class="text">${event.text}</p>
                <figure align="center" class="poza">
                    <img src="${event.fig.img}" alt="">
                    <figcaption>${event.fig.cap}</figcaption>
                </figure> 
                </div>
        </div>
        <div class="right"></div>
    `;
    card.innerHTML = cardContent;
    }

    container.appendChild(card);
  }
}
